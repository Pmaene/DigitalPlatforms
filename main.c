static inline void _writeModulus(unsigned char address) {
    while (P2 == 2) {}

    P2 = 0;
    P1 = address;

    P0 = INS_WRITE_MODULUS;
    P0 = INS_IDLE;

    while (P2 == 0) {}
    P0 = INS_ACK;
}

